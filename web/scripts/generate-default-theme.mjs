function cssColorName(name) {
  return name.replace(/[A-Z]/g, (letter) => `-${letter.toLowerCase()}`)
}

export function renderDefaultThemeTypeScript(theme) {
  return `// Generated by scripts/generate-default-theme.mjs from internal/project/default-theme.json.\n// Do not edit this file directly.\n\nimport type { ThemeSettings } from './types'\n\nexport const DEFAULT_THEME = ${JSON.stringify(theme, null, 2)} as const satisfies ThemeSettings\n`
}

export function renderDefaultThemeCSS(theme) {
  const variables = [
    `  --theme-font-family: ${theme.fontFamily};`,
    ...Object.entries(theme.colors).map(([name, value]) => (
      `  --theme-color-${cssColorName(name)}: ${value};`
    )),
  ]
  return `/* Generated by scripts/generate-default-theme.mjs from internal/project/default-theme.json. */\n/* Do not edit this file directly. */\n\n:root {\n${variables.join('\n')}\n}\n`
}

