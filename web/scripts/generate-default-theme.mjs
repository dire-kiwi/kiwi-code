import { readFile, writeFile } from 'node:fs/promises'
import { pathToFileURL } from 'node:url'

const sourceUrl = new URL('../../internal/project/default-theme.json', import.meta.url)
const typescriptUrl = new URL('../src/defaultTheme.generated.ts', import.meta.url)
const cssUrl = new URL('../src/default-theme.generated.css', import.meta.url)
const htmlUrl = new URL('../index.html', import.meta.url)

function cssColorName(name) {
  return name.replace(/[A-Z]/g, (letter) => `-${letter.toLowerCase()}`)
}

export function renderDefaultThemeTypeScript(theme) {
  return `// Generated by scripts/generate-default-theme.mjs from internal/project/default-theme.json.\n// Do not edit this file directly.\n\nimport type { ThemeSettings } from './types'\n\nexport const DEFAULT_THEME = ${JSON.stringify(theme, null, 2)} as const satisfies ThemeSettings\n`
}

export function renderDefaultThemeCSS(theme) {
  const variables = [
    `  --theme-font-family: ${theme.fontFamily};`,
    ...Object.entries(theme.colors).map(([name, value]) => (
      `  --theme-color-${cssColorName(name)}: ${value};`
    )),
  ]
  return `/* Generated by scripts/generate-default-theme.mjs from internal/project/default-theme.json. */\n/* Do not edit this file directly. */\n\n:root {\n${variables.join('\n')}\n}\n`
}

export async function readDefaultTheme() {
  return JSON.parse(await readFile(sourceUrl, 'utf8'))
}

export function renderIndexHTMLThemeColor(html, theme) {
  const next = html.replace(
    /(<meta name="theme-color" content=")[^"]*(" \/>)/,
    `$1${theme.colors.canvas}$2`,
  )
  if (next === html && !html.includes(`name="theme-color" content="${theme.colors.canvas}"`)) {
    throw new Error('web/index.html is missing its theme-color meta tag')
  }
  return next
}

export async function generateDefaultThemeFiles() {
  const [theme, html] = await Promise.all([
    readDefaultTheme(),
    readFile(htmlUrl, 'utf8'),
  ])
  await Promise.all([
    writeFile(typescriptUrl, renderDefaultThemeTypeScript(theme)),
    writeFile(cssUrl, renderDefaultThemeCSS(theme)),
    writeFile(htmlUrl, renderIndexHTMLThemeColor(html, theme)),
  ])
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  await generateDefaultThemeFiles()
}
